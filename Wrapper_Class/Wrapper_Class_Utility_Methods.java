package Wrapper_Class;

public class Wrapper_Class_Utility_Methods {
	public static void main(String[] args) {
		System.out.println("1.  .parseInt() converts the string to integer.");
		System.out.println("    Integer.parseInt(\"123\"); ----> "+Integer.parseInt("123"));
		System.out.println("2.  .parseDouble() converts the string to Double.");
		System.out.println("    Double.parseDouble(\"3.14\"); ----> "+Double.parseDouble("3.14"));
		System.out.println("3.  .parseBoolean() it cheaks the value is true or false.");
		System.out.println("    Boolean.parseBoolean(\"true\"); ----> "+Boolean.parseBoolean("true"));
		System.out.println("4.  .toBinaryString() is use to convert an integer value into its binary string representation.");
		System.out.println("    Integer.toBinaryString(10); ----> "+Integer.toBinaryString(10));
		System.out.println("5.  .isDigit() is use to check if the value is digit or not.");
		System.out.println("    Character.isDigit('5'); ----> "+Character.isDigit('5'));
		System.out.println("6.  .toUpperCase() is use convert the letters into uppercase.");
		System.out.println("    Character.toUpperCase('a'); ----> "+Character.toUpperCase('a'));
	}
}
